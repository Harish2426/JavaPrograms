package Harish;

public class Employee {
	private String name;
	private int id;
	private String city;
	void getName(String name) {
		this.name = name;
	}
    String setName() {
    	return name;
    }
    void getId(int id) {
    	this.id=id;
    }
    int setId() {
    	return id;
    }
    void getCity(String city) {
    	this.city=city;
    }
    String setCity() {
    	return city;
    }
}
    
